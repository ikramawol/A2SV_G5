class Solution(object):
    def multiply(self, num1, num2):
        """
        :type num1: str
        :type num2: str
        :rtype: str
        """
        nums = []
        nums. append(int(num1))
        nums .append(int(num2))
        ans = ""
        ans += str(nums[0]*nums[1])
        return ans 
        